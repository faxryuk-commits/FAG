export function detectCuisineType(name: string, cuisineTags: string[]): CuisineTemplate | null {
  const searchText = `${name} ${cuisineTags.join(' ')}`.toLowerCase();
  
  for (const template of CUISINE_TEMPLATES) {
    for (const keyword of template.keywords) {
      if (searchText.includes(keyword.toLowerCase())) {
        return template;
      }
    }
  }
  
  // –ü–æ —É–º–æ–ª—á–∞–Ω–∏—é - —É–∑–±–µ–∫—Å–∫–∞—è –∫—É—Ö–Ω—è (–¥–ª—è –£–∑–±–µ–∫–∏—Å—Ç–∞–Ω–∞)
  return CUISINE_TEMPLATES.find(t => t.id === 'uzbek') || null;
}

